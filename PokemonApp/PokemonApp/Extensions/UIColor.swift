import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var pokeYellow: UIColor {
        return UIColor(red: 255/255.0, green: 180/255.0, blue: 68/255.0, alpha: 1.0)
    }

    @nonobjc class var pokeGreen: UIColor {
        return UIColor(red: 178/255.0, green: 255/255.0, blue: 226/255.0, alpha: 1.0)
    }
    
    @nonobjc class var pokeBlue: UIColor {
        return UIColor(red: 173/255.0, green: 218/255.0, blue: 238/255.0, alpha: 1.0)
    }
    
    @nonobjc class var pokeCoal: UIColor {
        return UIColor(red: 35/255.0, green: 35/255.0, blue: 35/255.0, alpha: 1.0)
    }
    
    @nonobjc class var pokeCoalAlpha: UIColor {
        return UIColor(red: 35/255.0, green: 35/255.0, blue: 35/255.0, alpha: 0.975)
    }

    @nonobjc class var pokeBlack: UIColor {
        return UIColor(red: 0/255.0, green: 0/255.0, blue: 0/255.0, alpha: 1.0)
    }
    
    @nonobjc class var pokeWhite: UIColor {
        return UIColor(red: 255/255.0, green: 255/255.0, blue: 255/255.0, alpha: 1.0)
    }
    
}
